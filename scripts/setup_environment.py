def preprocess_mpiigaze_h5() -> None:
    h5_path = os.path.join(VENDOR_DIR, "pytorch_mpiigaze", "datasets", "MPIIGaze.h5")
    if os.path.isfile(h5_path):
        print(f"[skip] H5 exists: {h5_path}")
        return
    if not os.path.isdir(MPIIGAZE_DATA):
        print(
            f"\n[warn] MPIIGaze not found at:\n  {MPIIGAZE_DATA}\n"
            "Skipping MPIIGaze.h5 preprocess. Add dataset and re-run with --preprocess-h5."
        )
        return
    vendor = os.path.join(VENDOR_DIR, "pytorch_mpiigaze")
    run(
        [
            sys.executable,
            "tools/preprocess_mpiigaze.py",
            "--dataset",
            MPIIGAZE_DATA,
            "-o",
            "datasets",
        ],
        cwd=vendor,
    )


def download_gaze_weights() -> None:
    script = os.path.join(PROJECT_ROOT, "scripts", "download_pretrained_gaze_weights.py")
    run([sys.executable, script])


def print_next_steps() -> None:
    print(
        "\n"
        "=" * 60
        + "\nSetup complete (pip, vendor clones, licenses, gaze weights).\n\n"
        "Place MPIIGaze under data/original/MPIIGaze/ if not already done.\n\n"
        "Then run these three commands (project root):\n"
        "  python data/extract_normalized_images.py\n"
        "  python data/simulate_low_light.py\n"
        "  python evaluation/run_all.py\n"
        "\nLicenses: see THIRD_PARTY_NOTICES.md\n"
        "=" * 60
    )


def main() -> None:
    parser = argparse.ArgumentParser(description="Install deps and clone vendor repos.")
    parser.add_argument("--skip-pip", action="store_true", help="Skip pip install")
    parser.add_argument("--skip-clone", action="store_true", help="Skip git clone")
    parser.add_argument(
        "--skip-weights",
        action="store_true",
        help="Skip downloading pretrained gaze weights",
    )
    parser.add_argument(
        "--preprocess-h5",
        action="store_true",
        help="Build MPIIGaze.h5 in vendor/pytorch_mpiigaze (optional)",
    )
    args = parser.parse_args()

    os.chdir(PROJECT_ROOT)
    print("Project root:", PROJECT_ROOT)

    if not args.skip_pip:
        pip_install()

    if not args.skip_clone:
        clone_repos()

    write_third_party_notices()

    if not args.skip_weights:
        download_gaze_weights()

    if args.preprocess_h5:
        preprocess_mpiigaze_h5()

    print_next_steps()


if __name__ == "__main__":
    main()
