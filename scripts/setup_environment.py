def run(cmd: list[str], cwd: str | None = None) -> None:
    print(f"\n>> {' '.join(cmd)}")
    subprocess.check_call(cmd, cwd=cwd or PROJECT_ROOT)


def pip_install() -> None:
    run([sys.executable, "-m", "pip", "install", "-r", REQUIREMENTS])


def clone_repos() -> None:
    os.makedirs(VENDOR_DIR, exist_ok=True)
    for repo in VENDOR_REPOS:
        dest = os.path.join(VENDOR_DIR, repo["dir"])
        if os.path.isdir(os.path.join(dest, ".git")):
            print(f"[skip] already cloned: {repo['dir']}")
            continue
        run(["git", "clone", "--depth", "1", repo["url"], dest])
    patch_vendor_numpy_aliases()


def patch_vendor_numpy_aliases() -> None:
    """Fix np.float (removed in NumPy 2.x) in cloned vendor code if present."""
    mpiigaze = os.path.join(VENDOR_DIR, "pytorch_mpiigaze")
    if not os.path.isdir(mpiigaze):
        return
    replacements = [
        ("dtype=np.float)", "dtype=np.float64)"),
        ("dtype=np.float,", "dtype=np.float64,"),
        ("np.float)", "np.float64)"),
    ]
    patched = 0
    for root, _, files in os.walk(mpiigaze):
        for name in files:
            if not name.endswith(".py"):
                continue
            path = os.path.join(root, name)
            text = open(path, encoding="utf-8").read()
            new = text
            for old, new_val in replacements:
                new = new.replace(old, new_val)
            if new != text:
                open(path, "w", encoding="utf-8").write(new)
                patched += 1
    if patched:
        print(f"Patched {patched} vendor file(s) for NumPy 2.x compatibility.")


